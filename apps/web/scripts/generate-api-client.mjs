import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const webRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(webRoot, "..", "..");
const specPath = path.join(repoRoot, "contracts", "openapi", "openapi.yaml");
const outputPath = path.join(webRoot, "src", "shared", "api", "generated", "client.ts");

const httpMethods = ["get", "post", "put", "patch", "delete"];

async function main() {
  const specText = await fs.readFile(specPath, "utf8");
  const spec = YAML.parse(specText);

  const declarations = [];
  const exportedNames = new Set();

  for (const [schemaName, schema] of Object.entries(spec.components?.schemas ?? {})) {
    declarations.push(emitSchemaDeclaration(schemaName, schema));
    exportedNames.add(schemaName);
  }

  const methods = [];
  const routes = [];
  for (const [pathTemplate, pathItem] of Object.entries(spec.paths ?? {})) {
    for (const method of httpMethods) {
      const operation = pathItem?.[method];
      if (!operation) {
        continue;
      }

      const operationId = operation.operationId;
      if (!operationId) {
        throw new Error(`Missing operationId for ${method.toUpperCase()} ${pathTemplate}`);
      }

      const parameters = [...(pathItem.parameters ?? []), ...(operation.parameters ?? [])];
      const pathParameters = parameters.filter((parameter) => parameter.in === "path");
      const queryParameters = parameters.filter((parameter) => parameter.in === "query");
      const requestBodySchema = operation.requestBody?.content?.["application/json"]?.schema;
      const response = selectSuccessResponse(operation.responses ?? {});
      const responseSchema = response?.content?.["application/json"]?.schema;

      const responseType = responseSchema
        ? ensureNamedSchema(`${pascalCase(operationId)}Response`, responseSchema, declarations, exportedNames)
        : "void";
      const bodyType = requestBodySchema
        ? ensureNamedSchema(`${pascalCase(operationId)}RequestBody`, requestBodySchema, declarations, exportedNames)
        : "unknown";

      const optionsTypeName = `${pascalCase(operationId)}Options`;
      const routeOptionsTypeName = `${pascalCase(operationId)}RouteOptions`;
      const hasPath = pathParameters.length > 0;
      const hasQuery = queryParameters.length > 0;
      const hasBody = Boolean(requestBodySchema);
      const hasOptions = hasPath || hasQuery || hasBody;
      const requiredSections = hasPath || Boolean(operation.requestBody?.required);
      const hasRouteOptions = hasPath || hasQuery;
      const hasRequiredRouteOptions = hasPath || queryParameters.some((parameter) => parameter.required);

      if (hasOptions) {
        declarations.push(emitOperationOptionsType(optionsTypeName, pathParameters, queryParameters, bodyType, operation.requestBody?.required === true));
        exportedNames.add(optionsTypeName);
      }

      if (hasRouteOptions) {
        declarations.push(emitRouteOptionsType(routeOptionsTypeName, pathParameters, queryParameters));
        exportedNames.add(routeOptionsTypeName);
      }

      methods.push(emitMethod({
        method,
        pathTemplate,
        operationId,
        responseType,
        optionsTypeName,
        hasOptions,
        requiredSections,
      }));
      routes.push(emitRoute({
        pathTemplate,
        operationId,
        routeOptionsTypeName,
        hasRouteOptions,
        hasRequiredRouteOptions,
      }));
    }
  }

  const output = [
    "// This file is auto-generated by apps/web/scripts/generate-api-client.mjs.",
    "// Do not edit this file manually.",
    "",
    'import { getAdminKey } from "../../../lib/auth";',
    "",
    "type QueryValue = string | number | boolean | null | undefined;",
    "",
    declarations.join("\n\n"),
    "",
    'const API_BASE = (import.meta.env.VITE_API_BASE as string | undefined)?.replace(/\\/+$/, "") ?? "";',
    "",
    "type RequestOptions = {",
    "  method: string;",
    "  pathTemplate: string;",
    "  path?: Record<string, string | number>;",
    "  query?: Record<string, QueryValue>;",
    "  body?: unknown;",
    "};",
    "",
    "type RouteOptions = {",
    "  pathTemplate: string;",
    "  path?: Record<string, string | number>;",
    "  query?: Record<string, QueryValue>;",
    "};",
    "",
    "function adminHeaders(): HeadersInit {",
    "  const adminKey = getAdminKey();",
    "  if (!adminKey) {",
    "    return {};",
    "  }",
    '  return { "X-Admin-Key": adminKey };',
    "}",
    "",
    "function renderPath(pathTemplate: string, path?: Record<string, string | number>) {",
    "  if (!path) {",
    "    return pathTemplate;",
    "  }",
    "  return pathTemplate.replace(/\\{([^}]+)\\}/g, (_, key) => {",
    "    const value = path[key];",
    "    if (value === undefined || value === null) {",
    '      throw new Error(`Missing path parameter: ${key}`);',
    "    }",
    "    return encodeURIComponent(String(value));",
    "  });",
    "}",
    "",
    "function buildQuery(query?: Record<string, QueryValue>) {",
    "  if (!query) {",
    '    return "";',
    "  }",
    "  const params = new URLSearchParams();",
    "  for (const [key, value] of Object.entries(query)) {",
    '    if (value === undefined || value === null || value === "") {',
    "      continue;",
    "    }",
    "    params.set(key, String(value));",
    "  }",
    "  const rendered = params.toString();",
    '  return rendered ? `?${rendered}` : "";',
    "}",
    "",
    "export function buildApiURL({ pathTemplate, path, query }: RouteOptions): string {",
    '  return `${API_BASE}${renderPath(pathTemplate, path)}${buildQuery(query)}`;',
    "}",
    "",
    "async function request<T>({ method, pathTemplate, path, query, body }: RequestOptions): Promise<T> {",
    "  const response = await fetch(buildApiURL({ pathTemplate, path, query }), {",
    "    method,",
    "    headers: {",
    "      ...adminHeaders(),",
    '      ...(body !== undefined ? { "Content-Type": "application/json" } : {}),',
    "    },",
    "    ...(body !== undefined ? { body: JSON.stringify(body) } : {}),",
    "  });",
    "",
    "  const payload = await response.text();",
    "  if (!response.ok) {",
    '    throw new Error(payload || response.statusText);',
    "  }",
    "  if (payload.length === 0) {",
    "    return undefined as T;",
    "  }",
    '  const contentType = response.headers.get("content-type") ?? "";',
    '  if (contentType.includes("application/json")) {',
    "    return JSON.parse(payload) as T;",
    "  }",
    "  return payload as T;",
    "}",
    "",
    "export const client = {",
    methods.join(",\n"),
    "};",
    "",
    "export const apiRoutes = {",
    routes.join(",\n"),
    "};",
    "",
  ].join("\n");

  await fs.writeFile(outputPath, `${output}`);
}

function emitSchemaDeclaration(name, schema) {
  if (shouldEmitInterface(schema)) {
    return `export interface ${name} ${renderObjectShape(schema, 0)}`;
  }
  return `export type ${name} = ${renderType(schema, 0)};`;
}

function ensureNamedSchema(name, schema, declarations, exportedNames) {
  if (schema?.$ref) {
    return refName(schema.$ref);
  }
  if (exportedNames.has(name)) {
    return name;
  }
  declarations.push(emitSchemaDeclaration(name, schema));
  exportedNames.add(name);
  return name;
}

function emitOperationOptionsType(name, pathParameters, queryParameters, bodyType, isBodyRequired) {
  const lines = [];
  const hasRequiredQuery = queryParameters.some((parameter) => parameter.required);

  if (pathParameters.length > 0) {
    lines.push("  path: {");
    for (const parameter of pathParameters) {
      lines.push(`    ${quoteProperty(parameter.name)}: ${renderType(parameter.schema ?? {}, 2)};`);
    }
    lines.push("  };");
  }

  if (queryParameters.length > 0) {
    lines.push(`  query${hasRequiredQuery ? "" : "?"}: {`);
    for (const parameter of queryParameters) {
      const optional = parameter.required ? "" : "?";
      lines.push(`    ${quoteProperty(parameter.name)}${optional}: ${renderType(parameter.schema ?? {}, 2)};`);
    }
    lines.push("  };");
  }

  const bodyOptional = isBodyRequired ? "" : "?";
  if (bodyType !== "unknown" || isBodyRequired) {
    lines.push(`  body${bodyOptional}: ${bodyType};`);
  }

  if (lines.length === 0) {
    return `export type ${name} = Record<string, never>;`;
  }

  return [`export interface ${name} {`, ...lines, "}"].join("\n");
}

function emitRouteOptionsType(name, pathParameters, queryParameters) {
  const lines = [];
  const hasRequiredQuery = queryParameters.some((parameter) => parameter.required);

  if (pathParameters.length > 0) {
    lines.push("  path: {");
    for (const parameter of pathParameters) {
      lines.push(`    ${quoteProperty(parameter.name)}: ${renderType(parameter.schema ?? {}, 2)};`);
    }
    lines.push("  };");
  }

  if (queryParameters.length > 0) {
    lines.push(`  query${hasRequiredQuery ? "" : "?"}: {`);
    for (const parameter of queryParameters) {
      const optional = parameter.required ? "" : "?";
      lines.push(`    ${quoteProperty(parameter.name)}${optional}: ${renderType(parameter.schema ?? {}, 2)};`);
    }
    lines.push("  };");
  }

  if (lines.length === 0) {
    return `export type ${name} = Record<string, never>;`;
  }

  return [`export interface ${name} {`, ...lines, "}"].join("\n");
}

function emitMethod({ method, pathTemplate, operationId, responseType, optionsTypeName, hasOptions, requiredSections }) {
  const inputArgument = hasOptions
    ? `options${requiredSections ? "" : "?"}: ${optionsTypeName}`
    : "";
  const optionsExpression = hasOptions
    ? requiredSections
      ? "options"
      : "options ?? {}"
    : "{}";

  return `  ${operationId}: (${inputArgument}) => request<${responseType}>({\n    method: "${method.toUpperCase()}",\n    pathTemplate: "${pathTemplate}",\n    ...${optionsExpression},\n  })`;
}

function emitRoute({ pathTemplate, operationId, routeOptionsTypeName, hasRouteOptions, hasRequiredRouteOptions }) {
  const inputArgument = hasRouteOptions
    ? `options${hasRequiredRouteOptions ? "" : "?"}: ${routeOptionsTypeName}`
    : "";
  const optionsExpression = hasRouteOptions
    ? hasRequiredRouteOptions
      ? "options"
      : "options ?? {}"
    : "{}";

  return `  ${operationId}: (${inputArgument}) => buildApiURL({\n    pathTemplate: "${pathTemplate}",\n    ...${optionsExpression},\n  })`;
}

function shouldEmitInterface(schema) {
  return Boolean(schema) && !schema.$ref && (schema.type === "object" || schema.properties || schema.additionalProperties);
}

function renderType(schema, indentLevel) {
  if (!schema) {
    return "unknown";
  }

  if (schema.$ref) {
    return refName(schema.$ref);
  }

  if (schema.enum) {
    return schema.enum.map((value) => JSON.stringify(value)).join(" | ");
  }

  if (schema.oneOf) {
    return schema.oneOf.map((item) => renderType(item, indentLevel)).join(" | ");
  }

  if (schema.anyOf) {
    return schema.anyOf.map((item) => renderType(item, indentLevel)).join(" | ");
  }

  if (schema.allOf) {
    return schema.allOf.map((item) => renderType(item, indentLevel)).join(" & ");
  }

  let rendered;
  switch (schema.type) {
    case "string":
      rendered = "string";
      break;
    case "integer":
    case "number":
      rendered = "number";
      break;
    case "boolean":
      rendered = "boolean";
      break;
    case "array":
      rendered = `${wrapIfNeeded(renderType(schema.items ?? {}, indentLevel))}[]`;
      break;
    case "object":
      rendered = renderObjectType(schema, indentLevel);
      break;
    default:
      if (schema.properties || schema.additionalProperties) {
        rendered = renderObjectType(schema, indentLevel);
      } else {
        rendered = "unknown";
      }
  }

  if (schema.nullable) {
    return `${rendered} | null`;
  }
  return rendered;
}

function renderObjectType(schema, indentLevel) {
  if (!schema.properties && schema.additionalProperties) {
    const valueType = schema.additionalProperties === true
      ? "unknown"
      : renderType(schema.additionalProperties, indentLevel);
    return `Record<string, ${valueType}>`;
  }
  return renderObjectShape(schema, indentLevel);
}

function renderObjectShape(schema, indentLevel) {
  const indent = "  ".repeat(indentLevel);
  const propertyIndent = "  ".repeat(indentLevel + 1);
  const required = new Set(schema.required ?? []);
  const lines = ["{"];

  for (const [propertyName, propertySchema] of Object.entries(schema.properties ?? {})) {
    const optional = required.has(propertyName) ? "" : "?";
    lines.push(`${propertyIndent}${quoteProperty(propertyName)}${optional}: ${renderType(propertySchema, indentLevel + 1)};`);
  }

  if (schema.additionalProperties) {
    const additionalType = schema.additionalProperties === true
      ? "unknown"
      : renderType(schema.additionalProperties, indentLevel + 1);
    lines.push(`${propertyIndent}[key: string]: ${additionalType};`);
  }

  if (lines.length === 1) {
    lines.push(`${propertyIndent}[key: string]: unknown;`);
  }

  lines.push(`${indent}}`);
  return lines.join("\n");
}

function wrapIfNeeded(rendered) {
  if (rendered.includes("|") || rendered.includes("&") || rendered.includes("\n")) {
    return `(${rendered})`;
  }
  return rendered;
}

function quoteProperty(name) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

function refName(ref) {
  return ref.split("/").at(-1);
}

function pascalCase(value) {
  return String(value)
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join("");
}

function selectSuccessResponse(responses) {
  const entries = Object.entries(responses)
    .filter(([statusCode]) => /^2\d\d$/.test(statusCode))
    .sort(([left], [right]) => Number(left) - Number(right));
  return entries[0]?.[1];
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
